class Narrowing
{
    public static void main(String args[])
    {
        int i=10;
        byte b=(byte)i;
        System.out.println(b);

        int i1=97;
        char ch=(char)i1;
        System.out.println(ch);

        double d=2.5;
        int i2=(int)d;
        System.out.println(i2);

        int num=130;
        byte number=(byte)num;
        System.out.println(number);
    }
}